package com.asnj.entity;

public class Member {

	private int mem_pk;
	private String mem_user_id;
	private String mem_user_pw;
	private String mem_user_name;
	private String mem_user_email;
	private String mem_user_job;
	private String mem_time;
	
	public int getMem_pk() {
		return mem_pk;
	}
	public void setMem_pk(int mem_pk) {
		this.mem_pk = mem_pk;
	}
	public String getMem_user_id() {
		return mem_user_id;
	}
	public void setMem_user_id(String mem_user_id) {
		this.mem_user_id = mem_user_id;
	}
	public String getMem_user_pw() {
		return mem_user_pw;
	}
	public void setMem_user_pw(String mem_user_pw) {
		this.mem_user_pw = mem_user_pw;
	}
	public String getMem_user_name() {
		return mem_user_name;
	}
	public void setMem_user_name(String mem_user_name) {
		this.mem_user_name = mem_user_name;
	}
	public String getMem_user_email() {
		return mem_user_email;
	}
	public void setMem_user_email(String mem_user_email) {
		this.mem_user_email = mem_user_email;
	}
	public String getMem_user_job() {
		return mem_user_job;
	}
	public void setMem_user_job(String mem_user_job) {
		this.mem_user_job = mem_user_job;
	}
	public String getMem_time() {
		return mem_time;
	}
	public void setMem_time(String mem_time) {
		this.mem_time = mem_time;
	}
	
	
}
